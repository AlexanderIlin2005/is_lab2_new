package org.itmo.dto;

import lombok.Value;

/**
 * DTO для возврата результата операции импорта XML.
 * Используется для передачи количества импортированных объектов и статусного сообщения на фронтенд.
 */
@Value
public class ImportResultDto {
    /** Количество объектов, успешно добавленных в базу данных. */
    int imported;

    /** Сообщение о результате операции (для отображения пользователю). */
    String message;

    /** Флаг успеха операции. */
    boolean success;
}